const GermanB2Data = async (typeId: string) => {
  try {
    const formattedQuestions = questionsData.map((q) => ({
      typeId,
      subject: q.subject,
      level: q.level,
      questionText: q.text,
      options: q.options.map((opt, index) => ({
        text: opt,
        isCorrect: index === q.correct,
      })),
    }));

    await prisma.question.createMany({
      data: formattedQuestions,
      skipDuplicates: true,
    });
    console.log(
      `Successfully imported ${formattedQuestions.length} German B2 questions!`,
    );
  } catch (err) {
    console.error("German B2 Import failed:", err);
  }
};

export default GermanB2Data;
