const GermanC1Data = async (typeId: string) => {
  try {
    const formattedQuestions = questionsData.map((q) => ({
      typeId,
      subject: q.subject,
      level: q.level,
      questionText: q.text,
      options: q.options.map((opt, index) => ({
        text: opt,
        isCorrect: index === q.correct,
      })),
    }));

    await prisma.question.createMany({
      data: formattedQuestions,
      skipDuplicates: true,
    });
    console.log(
      `Successfully imported ${formattedQuestions.length} German C1 questions!`,
    );
  } catch (err) {
    console.error("German C1 Import failed:", err);
  }
};

export default GermanC1Data;
